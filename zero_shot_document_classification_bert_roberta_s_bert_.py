data_path = "/content/drive/MyDrive/bbc-500docs.csv"
data = pd.read_csv(data_path)

# Split the data into train and test sets
train_data, test_data, train_labels, test_labels = train_test_split(data['text'], data['category'], test_size=0.4, random_state=42)

# Load BERT tokenizer and model
tokenizer = BertTokenizer.from_pretrained('bert-base-uncased')
model = BertModel.from_pretrained('bert-base-uncased')

# Generate embeddings for train and test data
def generate_embeddings(texts):
    input_ids = tokenizer(texts, padding=True, truncation=True, return_tensors="pt", max_length=256)['input_ids']
    with torch.no_grad():
        embeddings = model(input_ids).pooler_output
    return embeddings

train_embeddings = generate_embeddings(train_data.tolist()).numpy()
test_embeddings = generate_embeddings(test_data.tolist()).numpy()

# Create a dictionary to store category embeddings
category_embeddings = {}

