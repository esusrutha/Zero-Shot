categories = ['business', 'entertainment', 'politics', 'sport', 'tech']
for category in categories:
    category_embeddings[category] = []

# Iterate through train data to calculate embeddings for each category
for i, category in enumerate(train_labels):
    category_embeddings[category].append(train_embeddings[i])

# Calculate average embeddings for each category
for category, embeddings_list in category_embeddings.items():
    category_embeddings[category] = np.mean(embeddings_list, axis=0)

# Initiali ze lists to store predicted labels and true labels
predicted_labels = []
true_labels = []

# Calculate and store predicted labels based on maximum cosine similarity (zero-shot classification)
for test_emb, true_label in zip(test_embeddings, test_labels):
    max_similarity = -1
